-module(emqx_ecq_test_helpers).

-export([
    all/1
]).

%%--------------------------------------------------------------------
%% API
%%--------------------------------------------------------------------

all(Module) ->
    [
        F
     || {F, _} <- Module:module_info(exports),
        is_test_function(F)
    ].

%%--------------------------------------------------------------------
%% Internal functions
%%--------------------------------------------------------------------

is_test_function(F) ->
    case atom_to_list(F) of
        "t_" ++ _ -> true;
        _ -> false
    end.


